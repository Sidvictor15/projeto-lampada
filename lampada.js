function ligar() {
    let lampada = document.getElementById('lampada');
    lampada.src = 'luz.png'; 
}

function desligar() {
    let lampada = document.getElementById('lampada');
    lampada.src = 'apagada.png'; 
}

function quebrar() {
    let lampada = document.getElementById('lampada');
    lampada.src = ''; 
}

